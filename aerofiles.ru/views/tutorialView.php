        <h5>3. Работа с файлами</h5>
        <h6>3.1. Загрузка файлов</h6>
        <p class="text">
          Загрузить файл в хранилище можно двумя способами:<br>
          1. Drag-and-Drop (Перетаскивание): Выделите файл в проводнике вашей ОС, зажмите левую кнопку мыши и переместите файл в «Область файлов» (1).<br>
          2. Через контекстное меню: Нажмите правую кнопку мыши в пустом месте Области файлов (1), в появившемся меню выберите пункт «Загрузить файл».<br>
          Отмена загрузки:<br>
          Если требуется отменить загрузку, наведите курсор на загружаемый файл, нажмите правую кнопку мыши и выберите пункт «Отмена».
        </p>
        <h6>3.2. Выделение файлов</h6>
        <p class="text">
          Одиночный файл: Наведите курсор на файл и нажмите левую кнопку мыши.<br>
          Несколько файлов: Удерживая клавишу Ctrl, нажимайте левой кнопкой мыши на нужные файлы.<br>
          Все файлы: Нажмите комбинацию клавиш Ctrl + A.
        </p>
        <h6>3.3. Удаление файлов</h6>
        <p class="text">
          1. Нажмите правой кнопкой мыши на файл.<br>
          2. В контекстном меню выберите пункт «Удалить».
        </p>
        <h6>3.4. Переименование файлов</h6>
        <p class="text">
          Переименовать файл можно одним из способов:<br>
          1. Двойной клик: Медленно нажмите два раза левой кнопкой мыши на имя файла.<br>
          2. Через меню: Нажмите правой кнопкой мыши на файл и выберите пункт «Переименовать»
        </p>
        <h6>3.5. Копирование и вставка</h6>
        <p class="text">
          Копирование:<br>
          Меню: Правая кнопка мыши на файле далее пункт «Копировать».<br>
          Горячие клавиши: Выделите файл и нажмите Ctrl + C.<br>
          Вставка:<br>
          Меню: Правая кнопка мыши в Области файлов (1) далее пункт «Вставить».<br>
          Горячие клавиши: Нажмите Ctrl + V.
        </p>
        <h6>3.6. Перемещение файлов</h6>
        <p class="text">
          Вырезание:<br>
          Меню: Правая кнопка мыши на файле далее пункт «Вырезать».<br>
          Горячие клавиши: Выделите файл и нажмите Ctrl + X.<br>
          Вставка:<br>
          Перейдите в нужную папку и используйте команду «Вставить» (через меню или Ctrl + V), как описано в пункте 3.5.
        </p>
        <h6>3.7. Скачивание файлов</h6>
        <p class="text">
          Для скачивания файла на локальное устройство:<br>
          1. Нажмите правой кнопкой мыши на файл.<br>
          2. Выберите пункт «Скачать».
        </p>
