        <p class="text">
          Открыть папку можно двумя способами:<br>
          1. Двойной клик: Нажмите два раза левой кнопкой мыши на иконку папки.<br>
          2. Через меню: Нажмите правой кнопкой мыши на папку и выберите пункт «Открыть».
        </p>
        <h6>4.3. Навигация по папкам</h6>
        <p class="text">
          Для перемещения по структуре директорий используйте Путь навигации (2):<br>
          Переход в папку: Нажмите левой кнопкой мыши на название папки в строке пути.<br>
          Переход в корень: Нажмите на элемент root в начале строки пути.
        </p>
        <h6>4.4. Избранное</h6>
        <p class="text">
          Вы можете добавить часто используемые папки в панель быстрого доступа.<br>
          Добавление в избранное: Нажмите правой кнопкой мыши на папку и выберите пункт «Добавить в избранное». Папка появится в Панели избранного (5).<br>
          Удаление из избранного: Нажмите правой кнопкой мыши на папку и выберите пункт «Удалить из избранного».
